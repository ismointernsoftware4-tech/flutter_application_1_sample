class StockStatusChart extends StatelessWidget {
  const StockStatusChart({super.key});

  @override
  Widget build(BuildContext context) {
    final stockStatus = Provider.of<DashboardProvider>(context).stockStatus;
    final total = stockStatus.active + stockStatus.lowStock;
    
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(8),
        boxShadow: [
          BoxShadow(
            color: Colors.grey.withValues(alpha: 0.1),
            spreadRadius: 1,
            blurRadius: 4,
            offset: const Offset(0, 2),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const Text(
            'Stock Status',
            style: TextStyle(
              fontSize: 16,
              fontWeight: FontWeight.bold,
              color: Colors.black87,
            ),
          ),
          const SizedBox(height: 20),
          SizedBox(
            height: 150,
            child: PieChart(
              PieChartData(
                sectionsSpace: 2,
                centerSpaceRadius: 50,
                sections: [
                  PieChartSectionData(
                    value: stockStatus.active.toDouble(),
                    color: Colors.green,
                    title: '${((stockStatus.active / total) * 100).toStringAsFixed(0)}%',
                    radius: 58,
                    titleStyle: const TextStyle(
                      fontSize: 14,
                      fontWeight: FontWeight.bold,
                      color: Colors.white,
                    ),
                  ),
                  PieChartSectionData(
                    value: stockStatus.lowStock.toDouble(),
                    color: Colors.orange,
                    title: '${((stockStatus.lowStock / total) * 100).toStringAsFixed(0)}%',
                    radius: 58,
                    titleStyle: const TextStyle(
                      fontSize: 14,
                      fontWeight: FontWeight.bold,
                      color: Colors.white,
                    ),
                  ),
                ],
              ),
            ),
          ),
          const SizedBox(height: 16),
          Row(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              _LegendItem(color: Colors.green, label: 'Active'),
              const SizedBox(width: 24),
              _LegendItem(color: Colors.orange, label: 'Low Stock'),
            ],
          ),
        ],
      ),
    );
  }
}

